package com.sellerapp.model;

public class VegetableMasterDTO {
	
	private Long vegId;
	private String vegetableName;
	private String vegetableSubtype;
	public Long getVegId() {
		return vegId;
	}
	public void setVegId(Long vegId) {
		this.vegId = vegId;
	}
	public String getVegetableName() {
		return vegetableName;
	}
	public void setVegetableName(String vegetableName) {
		this.vegetableName = vegetableName;
	}
	public String getVegetableSubtype() {
		return vegetableSubtype;
	}
	public void setVegetableSubtype(String vegetableSubtype) {
		this.vegetableSubtype = vegetableSubtype;
	}
	

}
